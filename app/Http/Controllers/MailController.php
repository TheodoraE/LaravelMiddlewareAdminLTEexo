    {
        $store = new Mail;
        $store->email = $request->email;
        $store->contenu = $request->contenu;
        $store->subject_id = $request->subject_id;
        $store->save();

        FacadesMail::to('tidoraa@gmail.com')->send(new MailSend($request));
        return redirect()->back();
    }

